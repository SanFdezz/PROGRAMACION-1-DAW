package ejercicios_C;
import java.util.*;
public class gato {

	private String nombre;
	private int edad;
	
	public gato(String nom, int ed) {
		nombre = nom;
		edad = ed;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public int getEdad() {
		return edad;
	}

	public void setEdad(int edad) {
		this.edad = edad;
	}
	
	
	
}
